function double(x) {
  return x * 2;
}
console.log(double(3));


function square(x) {
  return x * x;
}
console.log(square(2));


function isEven(x) {
  return x % 2 === 0;
}
console.log(isEven(2));
console.log(isEven(3));


function isOdd(x) {
  return x % 2 !== 0;
}
console.log(isOdd(2));
console.log(isOdd(3));